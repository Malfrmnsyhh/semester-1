function saklar1() {
    let toggle = document.getElementById("default-toggle1");
    let lampu1 = document.getElementById("lampu1")
    
    if (toggle.checked) {
        lampu1.src = "/aset/on.gif"
    } else {
        lampu1.src = "/aset/off.gif"
    }
}

